public class ViewpagerItem {

    String imageUrl;
    String castName;

    public void setImageUrl(String url) {
        imageUrl = url;
    }

    public void setCastName(String name) {
        castName = name;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getCastName() {
        return castName;
    }
}
